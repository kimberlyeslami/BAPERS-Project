     /**
     * Submit Button
     * <p>
     * creates a new backup file.
     */
    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
        if (jobRole.equals("Admin")){
            JOptionPane.showMessageDialog(null, "As Administrator, you only have access to the Restore funtionality!", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            Writer writer = new Writer("backupSchedule.txt");
            writer.writeBackupSchedule(schedule_cmbo.getSelectedItem().toString());
            JOptionPane.showMessageDialog(null, "Backup Schedule Updated!", "Success!", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_submit_btnActionPerformed
    
    /**Sort
    *<p>
    *Sorts through the list.
    */
    public void sortList() {
        ListModel list = bkup_lst.getModel();

        int size = list.getSize();

        String[] data = new String[size];
        for (int i = 0; i < size; i++) {
            data[i] = (String) list.getElementAt(i);
        }

        Arrays.sort(data);

        bkup_lst.setListData(data);
    }

