    /**
     * Restore Button
     * <p>
     * Restores from the database.
     */
    private void Restore_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Restore_btnActionPerformed
        if (bkup_lst.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null, "Please select a backup file to restore from!", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            myDB.restore(file.getPath() + "/" + bkup_lst.getSelectedValue());
        }
    }//GEN-LAST:event_Restore_btnActionPerformed
