    public CashPayment(String jobRole, Customer cust, Job job, String empID) {
        this.empID = empID;
        this.jobRole = jobRole;
        this.cust = cust;
        this.job = job;
        initComponents();
        
        DBConnectivity myDB = new DBConnectivity();
        myDB.connect();
        Connection con = myDB.getConn();
        try {
            con.setAutoCommit(false);
            ResultSet rs = myDB.read("SELECT `sub-total`, totalCost FROM Invoice WHERE invoice_ID = '" + job.getInvoiceID() + "'");
            
            while (rs.next()) {
                subTot = rs.getDouble("sub-total");
                totCost = rs.getDouble("totalCost");
                subTot_lbl.setText("Sub-Total: £" + df.format(subTot));
                totCost_lbl.setText("Total Cost (Inc. VAT): £" + df.format(totCost));
            }
            con.commit();
        } catch (SQLException ex) {
            if (con != null) {
                try {
                    System.err.print("Transaction rolling back!");
                    con.rollback();
                } catch (SQLException ex1) {
                    Logger.getLogger(DBConnectivity.class.getName()).log(Level.SEVERE, null, ex1);
                }
            }
        }finally{
            try {
                con.setAutoCommit(true);
            } catch (SQLException ex) {
                Logger.getLogger(CashPayment.class.getName()).log(Level.SEVERE, null, ex);
            }
            myDB.closeConnection();
        }
    }

