    /**
     * Amount text field restriction
     * <p>
     * Restricts the field to only allows numbers and '.' to be entered.
     */
    private void amount_txtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_amount_txtKeyTyped
        char vChar = evt.getKeyChar();
        if (!(Character.isDigit(vChar) || vChar == '.'
                || (vChar == KeyEvent.VK_DELETE))) {
            evt.consume();
        }
    }//GEN-LAST:event_amount_txtKeyTyped

