    /**
     * Create Job
     * <p>
     * Creates a new job and stores it into the database.
     */
    private void CreateJob_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CreateJob_btnActionPerformed
        if (SpecialInstructions_txt.getText().isEmpty() && deadline_dte.getDate() == null && Priority_cmbo.getSelectedItem().toString().isEmpty() && custID_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter All Fields!");
        } else {
            myDB.connect();
            Connection con = myDB.getConn();
            try {
                con.setAutoCommit(false);
                ResultSet rs = myDB.read("SELECT customer_ID FROM Customers WHERE customer_ID = '" + custID_txt.getText() + "'");
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(null, "Customer ID does not exists!", "Error!", JOptionPane.ERROR_MESSAGE);
                    custID_txt.setText("");
                } else {
                    Timestamp accepted = new Timestamp(System.currentTimeMillis());
                    Timestamp deadline = new Timestamp(deadline_dte.getDate().getTime());

                    Job job = new Job(SpecialInstructions_txt.getText(), Priority_cmbo.getSelectedItem().toString(), accepted, deadline, custID_txt.getText(), tasks);
                    JOptionPane.showMessageDialog(null, "Job Added ");
                }
                con.commit();
            } catch (SQLException ex) {
                Logger.getLogger(CreateJobs.class.getName()).log(Level.SEVERE, null, ex);
                if (con != null) {
                    try {
                        System.err.print("Transaction rolling back!");
                        con.rollback();
                    } catch (SQLException ex1) {
                        Logger.getLogger(DBConnectivity.class.getName()).log(Level.SEVERE, null, ex1);
                    }
                }
            }
            this.dispose();
            HomePage hp = new HomePage(jobRole, empID);
            hp.setVisible(true);
        }
    }//GEN-LAST:event_CreateJob_btnActionPerformed

