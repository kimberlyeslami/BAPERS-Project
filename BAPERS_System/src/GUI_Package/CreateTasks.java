    /**
     * Create tasks
     * <p>
     * Creates a new task and stores it into the database.
     */
    private void Create_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Create_btnActionPerformed
        String Price = Price_txt.getText();
        String Duration = Duration_txt.getText();
        if (Description_txt.getText().isEmpty() || Location_cmbo.getSelectedItem().toString().isEmpty() || ShelfSlot_txt.getText().isEmpty() || Price.isEmpty() || Duration.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Text in All Fields ");
        } else {
            Double price = Double.parseDouble(Price);
            Double duration = Double.parseDouble(Duration);
            Task task = new Task(Description_txt.getText(), Location_cmbo.getSelectedItem().toString(), ShelfSlot_txt.getText(),
                    price, duration);
            JOptionPane.showMessageDialog(null, "Task Added ");
            Tasks t = new Tasks(jobRole, empID);
            t.setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_Create_btnActionPerformed
    /**
     * Description text field restriction
     * <p>
     * Restricts the field to only allows characters to be entered.
     */
    private void Description_txtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Description_txtKeyTyped

        char vChar = evt.getKeyChar();
        if ((Character.isDigit(vChar)
                || (vChar == KeyEvent.VK_DELETE))) {
            evt.consume();
        }
    }//GEN-LAST:event_Description_txtKeyTyped
