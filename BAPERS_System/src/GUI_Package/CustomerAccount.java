    /**
     * Phone Number text field restriction
     * <p>
     * Restricts the field to only allows 11 numbers to be entered.
     */
    private void PhoneNumber_txtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_PhoneNumber_txtKeyTyped

        char vChar = evt.getKeyChar();
        if (!(Character.isDigit(vChar)
                || (vChar == KeyEvent.VK_DELETE))) {
            evt.consume();
        }

        if (PhoneNumber_txt.getText().length() >= 11) {
            evt.consume();
        }

    }//GEN-LAST:event_PhoneNumber_txtKeyTyped
