    public CustomerAccount(String jobRole, String empID) {
        this.empID = empID;
        this.jobRole = jobRole;
        initComponents();
        DBConnectivity myDB = new DBConnectivity();
        Random rand = new Random();
        custID = "ACC" + String.format("%04d", rand.nextInt(10000) + 1);
        myDB.connect();
        Connection con = myDB.getConn();
        try {
            con.setAutoCommit(false);

            boolean isDuplicate = true;
            while (isDuplicate) {
                ResultSet rs = myDB.read("SELECT customer_ID FROM Customers WHERE customer_ID = '" + custID + "'");
                try {
                    if (!rs.next()) {
                        isDuplicate = false;
                    } else {
                        custID = "ACC" + String.format("%04d", rand.nextInt(10000) + 1);
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(Customer.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            CustomerID_lbl.setText("Customer ID: " + custID);
            con.commit();
        }catch (SQLException ex) {
            if (con != null) {
                try {
                    System.err.print("Transaction rolling back!");
                    con.rollback();
                } catch (SQLException ex1) {
                    Logger.getLogger(DBConnectivity.class.getName()).log(Level.SEVERE, null, ex1);
                }
            }
        }finally{
            try {
                con.setAutoCommit(true);
            } catch (SQLException ex) {
                Logger.getLogger(CustomerAccount.class.getName()).log(Level.SEVERE, null, ex);
            }
            myDB.closeConnection();
        }
    }

