    /**
     * Register Customer
     * <p>
     * Creates a new Customer and stores it in the database
     */
    private void Register_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Register_btnActionPerformed

        if (FirstName_txt.getText().isEmpty() && Surname_txt.getText().isEmpty() && PhoneNumber_txt.getText().isEmpty() && Email_txt.getText().isEmpty() && Address_txt.getText().isEmpty() && City_txt.getText().isEmpty() && Country_txt.getText().isEmpty() && PostCode_txt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please Enter Text in All Fields ");
        } else {
            Customer Customer = new Customer(custID, FirstName_txt.getText(), Surname_txt.getText(), Address_txt.getText(), City_txt.getText(),
                    Country_txt.getText(), PostCode_txt.getText(), Email_txt.getText(), PhoneNumber_txt.getText());
            JOptionPane.showMessageDialog(null, "Customer Registered ");
            this.dispose();
            HomePage hp = new HomePage(jobRole, empID);
            hp.setVisible(true);

        }
    }//GEN-LAST:event_Register_btnActionPerformed

