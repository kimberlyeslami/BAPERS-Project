     /**
     * Generate Button
     * <p>
     * Generates the Reports.
     */
    private void btnGenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateActionPerformed
        String report = coboxSelectReport.getSelectedItem().toString();
        if(report.equals("Individual Report")){
            IndividualReportForm ir = new IndividualReportForm(this, jobRole, CustID_Field.getText(), 
                    new java.sql.Date(DateStartDate.getDate().getTime()), new java.sql.Date(DateEndDate.getDate().getTime()), empID);
            ir.setVisible(true);
        } else if(report.equals("Individual Performance Report")){
            IndividualPerformanceReportForm ipr = new IndividualPerformanceReportForm(this, jobRole, 
                    new java.sql.Date(DateStartDate.getDate().getTime()), new java.sql.Date(DateEndDate.getDate().getTime()), empID);
            ipr.setVisible(true);
        } else {
            SummaryPerformanceReportForm spr = new SummaryPerformanceReportForm(this, jobRole, 
                    new java.sql.Date(DateStartDate.getDate().getTime()), new java.sql.Date(DateEndDate.getDate().getTime()), empID);
            spr.setVisible(true);
        }
    }//GEN-LAST:event_btnGenerateActionPerformed
