     /** Select report items
      * <p>
      * Sets the text field to visible when individual report is selected.
      */
    private void coboxSelectReportItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_coboxSelectReportItemStateChanged
        String s = coboxSelectReport.getSelectedItem().toString();
        if (s.equals("None")){
            DateStartDate.setEnabled(false);
            DateEndDate.setEnabled(false);
            CustID_Field.setEnabled(false);
        }else if (s.equals("Individual Report")){
            DateStartDate.setEnabled(true);
            DateEndDate.setEnabled(true);
            CustID_Field.setEnabled(true);
        } else {
            DateStartDate.setEnabled(true);
            DateEndDate.setEnabled(true);
            CustID_Field.setEnabled(false);
        }
    }//GEN-LAST:event_coboxSelectReportItemStateChanged
