     /**
     * Sort Table
     * <p>
     * Sorts through the table with the customers data.
     */
    public void sort(){
        DefaultTableModel dm = (DefaultTableModel) valued_tbl.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(dm);
        valued_tbl.setRowSorter(trs);
    }
     /**
     * Filter Table
     * <p>
     * Filters through the list with the Customers data.
     */
    public void filter(String s){
        DefaultTableModel dm = (DefaultTableModel) valued_tbl.getModel();
        TableRowSorter<DefaultTableModel> trs = new TableRowSorter<>(dm);
        valued_tbl.setRowSorter(trs);
        
        if (!"None".equals(s) || s.isEmpty()){
            trs.setRowFilter(RowFilter.regexFilter(s));
        } else {
            valued_tbl.setRowSorter(trs);
        }
    }
     /**
     * Table
     * <p>
     * Converts a result set to a table model
     * @param rs
     * @return converted result set.
     */
    public static DefaultTableModel resultSetToTableModel(ResultSet rs) {
        ResultSetMetaData md;
        
        Vector<String> columnNames = new Vector<>();
        Vector<Vector<Object>> data = new Vector<>();
        
        try {
            md = rs.getMetaData();
            
            // names of columns
            int columnCount = md.getColumnCount();
            for (int i = 1; i <= columnCount; i++) {
                columnNames.add(md.getColumnLabel(i));
            }

            // data of the table
            while (rs.next()) {
                Vector<Object> vector = new Vector<>();
                for (int i = 1; i <= columnCount; i++) {
                    vector.add(rs.getObject(i));
                }
                data.add(vector);
            }
        
        } catch (SQLException ex) {
            Logger.getLogger(InspectTasks.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return new DefaultTableModel(data, columnNames);
    }
    /**
     * Change account status
     * <p>
     * Changes the selected customers account status.
     * 
     * @param status customers status
     */
    public void changeAccountStatus(String status){
        int row = valued_tbl.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(null, "Please select a Customer!", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            Customer cust = new Customer(valued_tbl.getValueAt(row, 0).toString());
            cust.changeStatus(status);
            valued_tbl.setValueAt(status, row, 3);
        }
    }
    
