    /** Get Question Button
     * <p>
     * Gets the question from the employee id.
     * 
     */
    private void GetQuestion_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GetQuestion_btnActionPerformed
        
        myDB.connect();
        String empID = EmployeeID_txt.getText();
        ResultSet rs = myDB.read("select security_question from employee where employee_ID='" + empID + "'");
        try {
            while (rs.next()) {
                Question_lbl.setText(rs.getString("security_question"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(Verification.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_GetQuestion_btnActionPerformed

