    /** Submit Button 
    * <p>
    * checks whether the question and answer match 
    * takes you to the reset password form
    */
    private void Submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Submit_btnActionPerformed

       
        myDB.connect();
        String question = Question_lbl.getText();
        String answer = Answer_txt.getText();
        String empID = EmployeeID_txt.getText();
        ResultSet rs = myDB.read("select security_answer from employee where employee_ID='" + empID + "'");
        
        try {
            while(rs.next()){
                if(answer.equals(rs.getString("security_answer"))){
                    this.dispose();
                    ResetPassword rp = new ResetPassword(empID);
                    rp.setVisible(true);
                } else {
                  JOptionPane.showMessageDialog(null, "Incorrect Answer");
              }
            }
        } catch (SQLException ex) {
            Logger.getLogger(Verification.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_Submit_btnActionPerformed
