     /** view button
     * <p>
     * views the report.
     */
    private void view_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_view_btnActionPerformed
        String[] selectedRept = reports_lst.getSelectedValue().split(" - ");
        String selectedReportDate = selectedRept[0];
        String selectedReport = selectedRept[1];

        for(String s : reports){
            try {
                String[] report = s.split(",");
                String dateTime = report[0];
                String name = report[1];
                
                java.util.Date strtDate = sdf.parse(report[2]);
                java.sql.Date start = new java.sql.Date(strtDate.getTime());
                
                java.util.Date endDate = sdf.parse(report[3]);
                java.sql.Date end = new java.sql.Date(endDate.getTime());
                
                String custID = report[4];
            
                if(name.matches(selectedReport) && dateTime.matches(selectedReportDate)){
                    switch (name){
                        case "Individual Report":
                            this.dispose();
                            IndividualReportForm ir = new IndividualReportForm(this, jobRole, custID, start, end, empID);
                            ir.setVisible(true);
                            break;
                        case "Individual Performance Report":
                            this.dispose();
                            IndividualPerformanceReportForm ipr = new IndividualPerformanceReportForm(this, jobRole, start, end, empID);
                            ipr.setVisible(true);
                            break;
                        case "Summary Performance Report":
                            this.dispose();
                            SummaryPerformanceReportForm spr = new SummaryPerformanceReportForm(this,jobRole, start, end, empID);
                            spr.setVisible(true);
                            break; 
                    }
                }
            } catch (ParseException ex) {
                Logger.getLogger(View_Reports.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_view_btnActionPerformed
