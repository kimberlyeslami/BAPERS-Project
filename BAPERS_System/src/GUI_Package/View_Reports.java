    /** Filter name
     * <p>
     * Filters the list with the name entered.
     */
    private void filterName(String s){
        DefaultListModel lstModel = new DefaultListModel();
        
        for(String string : reports){
            try {
                String[] report = string.split(",");
                String dateTime = report[0];
                String name = report[1];
                
                java.util.Date strtDate = sdf.parse(report[2]);
                java.sql.Date start = new java.sql.Date(strtDate.getTime());
                
                java.util.Date endDate = sdf.parse(report[3]);
                java.sql.Date end = new java.sql.Date(endDate.getTime());
                
                String custID = report[4];
                
                if(s.equals("None")){
                    lstModel.addElement(dateTime + " - " + name + " - " + start + " - " + end + " - " + custID);
                } else if(name.equals(s)){
                    lstModel.addElement(dateTime + " - " + name + " - " + start + " - " + end + " - " + custID);
                }
            } catch (ParseException ex) {
                Logger.getLogger(View_Reports.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        reports_lst.setModel(lstModel);
    }
    /** Filter date
     * <p>
     * Filters the list with the date entered.
     */
    private void filterDate(String s){
        DefaultListModel lstModel = new DefaultListModel();
        
        for(String string : reports){
            try {
                String[] report = string.split(",");
                String dateTime = report[0];
                String name = report[1];
                
                java.util.Date strtDate = sdf.parse(report[2]);
                java.sql.Date start = new java.sql.Date(strtDate.getTime());
                
                java.util.Date endDate = sdf.parse(report[3]);
                java.sql.Date end = new java.sql.Date(endDate.getTime());
                
                String custID = report[4];
                
                if(s.equals("")){
                    lstModel.addElement(dateTime + " - " + name + " - " + start + " - " + end + " - " + custID);
                } else if(dateTime.contains(s)){
                    lstModel.addElement(dateTime + " - " + name + " - " + start + " - " + end + " - " + custID);
                }
            } catch (ParseException ex) {
                Logger.getLogger(View_Reports.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        reports_lst.setModel(lstModel);
    }
    
